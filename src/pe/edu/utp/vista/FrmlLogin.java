    private void loguearse(){
        if (txtEmail.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un email", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return;
        } 
        
        if (txtClave.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar una clave", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtClave.requestFocus();
            return;
        }
        
        UsuarioControl control=new UsuarioControl();
        String resp=control.login(txtEmail.getText(), txtClave.getText());
        if (resp.equals("1")) {
            this.dispose();//Ocultar
            FrmlPrincipal frml=new FrmlPrincipal();
            frml.toFront();//Traer alfrente
            frml.setVisible(true);            
        } else if(resp.equals("2")){
            JOptionPane.showMessageDialog(this, "Usuario no tiene acceso", "Sistema", JOptionPane.ERROR_MESSAGE);
        } else{
            JOptionPane.showMessageDialog(this, "Los datos son incorrectos", "Sistema", JOptionPane.ERROR_MESSAGE);
            txtEmail.setText("");
            txtEmail.requestFocus();
            txtClave.setText("");            
        }
    }
